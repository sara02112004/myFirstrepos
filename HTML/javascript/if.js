let temperature = 34;

if (temperature < 0) {
  console.log("It's freezing! Wear a heavy coat.");
} else if (temperature <= 20) {
  console.log("It's cool. Wear a jacket.");
} else if (temperature <= 30) {
  console.log("It's warm. Wear a T-shirt.");
} else {
  console.log("It's hot! Wear shorts.");
}
